def getNumberOfTrees(input,right,down):
    x = 0
    trees = 0
    for i in range(0,len(input),down):
        if x >= len(input[i]):
            x-=len(input[i])
        if input[i][x] == "#":
            trees += 1
        x+=right
    return trees
    
